#include <unistd.h>

void	ft_putstr(char *str)
{
	char *p;

	p = str;
	while (*p != 0)
	{
		write(1, p, 1);
		p++;
	}
}

int		main(int argc, char **argv)
{
	if (argc > 0)
		ft_putstr(argv[0]);
	write(1, "\n", 1);
	return (0);
}
