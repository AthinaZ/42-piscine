#include <unistd.h>

int	ft_atoi(char *str)
{
	int num;
	int i;
	int sign;

	i = 0;
	num = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num *= 10;
		num += (int)(str[i] - '0');
		i++;
	}
	return (num * sign);
}
